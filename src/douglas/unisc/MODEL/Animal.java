package douglas.unisc.MODEL;

public class Animal {
	
	private int head; // tamanho
	private int headFormat; //formato da cabeça
	private int faceColor;
	private int chest;
	private int memberHeight; // comprimento do membro
	private int memberRobustness; // robustez dos membros
	private int feet; //cor
	public int getHead() {
		return head;
	}
	public void setHead(int head) {
		this.head = head;
	}
	public int getHeadFormat() {
		return headFormat;
	}
	public void setHeadFormat(int headFormat) {
		this.headFormat = headFormat;
	}
	public int getFaceColor() {
		return faceColor;
	}
	public void setFaceColor(int faceColor) {
		this.faceColor = faceColor;
	}
	public int getChest() {
		return chest;
	}
	public void setChest(int chest) {
		this.chest = chest;
	}
	public int getMemberHeight() {
		return memberHeight;
	}
	public void setMemberHeight(int memberHeight) {
		this.memberHeight = memberHeight;
	}
	public int getMemberRobustness() {
		return memberRobustness;
	}
	public void setMemberRobustness(int memberRobustness) {
		this.memberRobustness = memberRobustness;
	}
	public int getFeet() {
		return feet;
	}
	public void setFeet(int feet) {
		this.feet = feet;
	}
	
	
	
	

}
